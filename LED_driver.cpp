void senddata(byte data)
{
    byte i;
    for(i = 0; i < 8; i++)
    {
        digitalWrite(CLK, 0);
        if(data & 0x80)
            digitalWrite(R1, 0);
        else
            digitalWrite(R1, 1);
        data = data << 1;
        digitalWrite(CLK, 1);
    }
}
void scan(byte row)
{
    digitalWrite(SignA, (row & 0x01));
    digitalWrite(SignB, (row & 0x02));
    digitalWrite(SignC, (row & 0x04));
    digitalWrite(SignD, (row & 0x08));
}
void show(byte msg[])
{
    byte i, j;
    for(i = 0; i < 16; i++)
    {
        for(j = 0; j < 8; j++)
            senddata(msg[i * 8 + j]);
        digitalWrite(EN, 1);
        scan(i);
        digitalWrite(LAT, 0);
        digitalWrite(LAT, 1);
        digitalWrite(EN, 0);
    }
}

